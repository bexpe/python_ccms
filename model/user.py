class User():
    def __init__(self, user_id, name, surname, email, date_of_birth, city, phone, login):
        self.user_id = user_id
        self.email = email
        self.surname = surname
        self.name = name
        self.date_of_birth = date_of_birth
        self.city = city
        self.phone = phone
        self.login = login
